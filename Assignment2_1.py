def add(num1,num2):
    addition=num1+num2;
    return addition;


def sub(num1,num2):
    substraction=num1-num2;
    return substraction;


def mult(num1,num2):
    multiplication=num1*num2;
    return multiplication;


def div(num1,num2):
    division=num1/num2;
    return division;

